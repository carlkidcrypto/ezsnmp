def test_session_bulk_walk_none_oids(sess):
    """Test that Session.bulk_walk(None) is treated same as empty list."""
    if sess.version == "1":
        with pytest.raises(PacketError):
            sess.bulk_walk(None)
    else:
        res = sess.bulk_walk(None)
        assert res is not None
